import chalk from "chalk";
console.log('My chalk app is starting!');

console.log(chalk.bold("Hello Sheryl!"));
console.log(chalk.green("Success message"));
console.log(chalk.redBright("Error: Something went wrong!"));
console.log(chalk.yellow.underline("Warning! Be careful!"));
console.log(chalk.bgBlue.white("ℹ️ Info: Chalk is working beautifully!"));
console.log(chalk.blue("This is a blue message!"));
console.log(chalk.magenta("This is a magenta message!"));
console.log(chalk.cyan("This is a cyan message!"));
console.log(chalk.whiteBright("This is a bright white message!"));  
console.log(chalk.black.bgYellow("This is a black text on yellow background!"));
console.log(chalk.gray("This is a gray message!"));
console.log(chalk.bold.italic("This is a bold and italic message!"));
console.log(chalk.inverse("This is an inverted message!"));
console.log(chalk.strikethrough("This is a strikethrough message!"));

